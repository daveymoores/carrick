pub struct SourceLocation {
    /// File path relative to repo root
    pub file_path: String,
    /// Start line (1-based)
    pub start_line: u32,
    /// End line (1-based)
    pub end_line: u32,
    /// Start column (0-based)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub start_column: Option<u32>,
    /// End column (0-based)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub end_column: Option<u32>,
}

/// An entry in the type manifest
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ManifestEntry {
    /// The alias or original name of the type
    pub alias: String,
    /// The original symbol name
    pub original_name: String,
    /// The source file where the type was found
    pub source_file: String,
    /// The full type definition string
    pub type_string: String,
    /// Whether this was an explicit annotation or inferred
    pub is_explicit: bool,
}

/// An inferred type result
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct InferredType {
    /// The alias for this type (generated if not provided)
    pub alias: String,
    /// The full TypeScript type string
    pub type_string: String,
    /// Whether the type was explicitly annotated in source
    pub is_explicit: bool,
    /// Source location information
    pub source_location: SourceLocation,
    /// The kind of inference that was performed
    pub infer_kind: InferKind,
    /// Deterministic source symbol of the resolved type (`Payment`), from the
    /// sidecar's `getSymbol() || getAliasSymbol()` name with TS/lib globals
    /// filtered out. Fills the manifest anchor (`primary_type_symbol`) when the
    /// LLM emitted none, removing the anchor's sole dependency on the model.
    /// For an array type this is the ELEMENT's symbol (`TimelineEvent` for
    /// `TimelineEvent[]`), with the peeled levels reported in `array_depth`.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub primary_type_symbol: Option<String>,
    /// Array levels the resolved type wraps around `primary_type_symbol`
    /// (#306): `TimelineEvent[]` reports symbol `TimelineEvent` + depth 1.
    /// `resolve_all_types` copies this onto an explicit `SymbolRequest` for the
    /// same alias/symbol so the bundle keeps the use-site's array-ness instead
    /// of the bare element. Absent when 0 or when there is no anchor symbol.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub array_depth: Option<u32>,
    /// Declaration file (absolute path) of `primary_type_symbol`, reported by
    /// the pub/sub infer kinds (`function_param`, `expression`) only (#413).
    /// `demote_witnessed_borrowed_anchors` uses it to re-aim a demoted
    /// explicit `SymbolRequest`: the bundler resolves a symbol only against
    /// declarations IN the request's `source_file`, and the inference is the
    /// only party that knows where the tsc-witnessed payload type lives.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub primary_type_symbol_source: Option<String>,
}

/// Information about a symbol that failed to resolve
