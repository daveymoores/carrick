//! TypeSidecar - Rust interface to the Node.js type extraction sidecar.
//!
//! Note: Some public APIs are not yet used but will be integrated in Phase 3.
#![allow(dead_code)]
//!
//! This module provides a non-blocking interface to spawn and communicate with
//! the type-sidecar process. The sidecar runs ts-morph and dts-bundle-generator
//! to extract TypeScript types from source code.
//!
//! Key design principles:
//! - **Non-blocking spawn**: The sidecar spawns immediately, initialization happens in background
//! - **Parallel startup**: SWC scanning and LLM analysis can proceed while sidecar initializes
//! - **JSON message protocol**: All communication is via stdin/stdout JSON messages

use serde::{Deserialize, Serialize};
use std::collections::{HashMap, HashSet};
use std::io::{BufRead, BufReader, Write};
use std::path::Path;
use std::process::{Child, ChildStdin, ChildStdout, Command, Stdio};
use std::sync::{Arc, Mutex};
use std::thread;
use std::time::{Duration, Instant};
use tracing::{debug, info};

// ============================================================================
// Sidecar State
// ============================================================================

/// State of the sidecar process
#[derive(Debug, Clone, PartialEq)]
pub enum SidecarState {
    /// Process has been spawned but not yet initialized
    Spawning,
    /// Init request sent, waiting for ready response
    Initializing,
    /// Sidecar is ready to process requests
    Ready,
    /// Sidecar failed to initialize or encountered an error
    Failed(String),
}

// ============================================================================
// Request Types (matching TypeScript types)
// ============================================================================

/// Kind of type inference to perform
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
#[serde(rename_all = "snake_case")]
pub enum InferKind {
    /// Get return type of a function
    FunctionReturn,
    /// Get type of an expression
    Expression,
    /// Get return type of a call expression
    CallResult,
    /// Get type of a variable declaration
    Variable,
    /// Find response body (.json()/.send()/ctx.body)
    ResponseBody,
    /// Find request body (req.body/ctx.request.body or call payloads)
    RequestBody,
    /// Function return for the signature hint — NO Promise/wrapper unwrapping
    SignatureReturn,
    /// Type of a single named parameter (explicit or contextually inferred)
    FunctionParam,
}

/// A rule for unwrapping machinery/wrapper types to extract payload types.
/// Generated by the cloud's `extraction_config` task from the repo's
/// detected frameworks, data fetchers, and dependency list; consumed by the
/// sidecar's agent-informed unwrap engine (`type-inferrer.ts`).
///
/// Wire format is camelCase to match the sidecar's `ExtractionRule`
/// (`src/sidecar/src/types.ts`). `payload_generic_index` must be omitted
/// (not null) when absent — the sidecar validator rejects null.
#[derive(Debug, Clone, Default, Serialize, Deserialize, PartialEq)]
#[serde(rename_all = "camelCase")]
pub struct ExtractionRule {
    /// Exact wrapper type/symbol names to unwrap (e.g. `AxiosResponse`).
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub wrapper_symbols: Vec<String>,
    /// Method/property names that mark a machinery type (e.g. `statusCode`).
    /// Only applied together with `origin_module_globs`.
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub machinery_indicators: Vec<String>,
    /// Module-path globs the wrapper's declaration must come from. Multiple
    /// candidate globs are allowed (ambient/global machinery types ship
    /// several plausible origins); entries that match nothing are no-ops.
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub origin_module_globs: Vec<String>,
    /// Index of the generic type argument holding the payload (default 0).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub payload_generic_index: Option<u32>,
    /// Property path to the payload when generics are unavailable
    /// (e.g. `["data"]` for `AxiosResponse.data`).
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub payload_property_path: Vec<String>,
    /// Recursively unwrap nested wrappers (`Promise<AxiosResponse<T>>` → `T`).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub unwrap_recursively: Option<bool>,
    /// Maximum unwrap depth when `unwrap_recursively` is set (sidecar
    /// defaults to 4).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub max_depth: Option<u32>,
}

/// Agent-generated configuration for extracting payload types from
/// machinery wrappers.
#[derive(Debug, Clone, Default, Serialize, Deserialize, PartialEq)]
pub struct ExtractionConfig {
    pub rules: Vec<ExtractionRule>,
}

/// Request for a specific symbol to be bundled
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct SymbolRequest {
    /// The name of the symbol (type, interface, class, etc.)
    pub symbol_name: String,
    /// The source file path (relative to repo root)
    pub source_file: String,
    /// Optional alias for the exported type
    #[serde(skip_serializing_if = "Option::is_none")]
    pub alias: Option<String>,
    /// Wrap the bundled symbol in this many TS array levels (#248): a GraphQL
    /// SDL producer field `[Order!]!` backed by `interface Order` bundles `Order`
    /// with `array_depth: 1` so the sidecar emits `Order[]` — the element type
    /// carries the shape, the SDL list marker carries the depth. Omitted/`0`
    /// bundles the symbol as-is (the HTTP/socket/consumer default).
    #[serde(skip_serializing_if = "Option::is_none")]
    pub array_depth: Option<u32>,
    /// Deterministic borrow witness from the scanner's AST pre-pass (pub/sub
    /// two-anchor arbitration, #413 / the #403/#359 borrow-class lineage):
    /// `true` means the file's own annotations testify that `symbol_name`
    /// structurally cannot be the payload this request's alias stands for —
    /// the payload binding is annotated with a type that never mentions the
    /// symbol, while the symbol annotates a DIFFERENT binding in the same
    /// file (where the model borrowed it from). Only
    /// `collect_pubsub_type_requests` ever sets this; HTTP/socket/GraphQL
    /// requests always carry `false`, so `demote_witnessed_borrowed_anchors`
    /// is structurally inert for every non-pub/sub path. Scanner-internal:
    /// never serialized to the sidecar.
    #[serde(skip)]
    pub payload_borrow_witness: bool,
}

/// Request for type inference at a specific location
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct InferRequestItem {
    /// Path to the file (relative to repo root)
    pub file_path: String,
    /// Line number (1-based) where inference should occur
    pub line_number: u32,
    /// Start byte offset of the target expression (from SWC spans)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub span_start: Option<u32>,
    /// End byte offset of the target expression (from SWC spans)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub span_end: Option<u32>,
    /// Verbatim expression text to locate in source (from Gemini)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub expression_text: Option<String>,
    /// Line number where the expression starts (from Gemini)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub expression_line: Option<u32>,
    /// The kind of inference to perform
    pub infer_kind: InferKind,
    /// Optional alias for the inferred type
    #[serde(skip_serializing_if = "Option::is_none")]
    pub alias: Option<String>,
    /// Target parameter name for `FunctionParam` inference.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub param_name: Option<String>,
}

