/// TypeSidecar - Manages communication with the Node.js type extraction sidecar.
///
/// # Example
///
/// ```no_run
/// use carrick::services::TypeSidecar;
/// use std::path::Path;
/// use std::time::Duration;
///
/// // Spawn the sidecar (non-blocking)
/// let mut sidecar = TypeSidecar::spawn(Path::new("./node_modules/.bin/type-sidecar")).unwrap();
///
/// // Start initialization (non-blocking)
/// sidecar.start_init(Path::new("/path/to/repo"), None);
///
/// // ... do other work while sidecar initializes ...
///
/// // Wait for sidecar to be ready
/// sidecar.wait_ready(Duration::from_secs(30)).unwrap();
///
/// // Now resolve types
/// let result = sidecar.resolve_types(&[]).unwrap();
/// ```
pub struct TypeSidecar {
    /// Child process handle
    child: Child,
    /// Stdin for sending requests (wrapped in mutex for thread safety)
    stdin: Mutex<ChildStdin>,
    /// Stdout for reading responses (wrapped in mutex for thread safety)
    stdout: Mutex<BufReader<ChildStdout>>,
    /// Current state of the sidecar
    state: Arc<Mutex<SidecarState>>,
    /// Time when spawn() was called
    spawn_time: Instant,
    /// Request ID counter
    request_counter: Mutex<u64>,
}

impl TypeSidecar {
    /// Spawn the sidecar process.
    ///
    /// This returns immediately after spawning - the sidecar process starts
    /// initializing in the background. Call `start_init()` to begin TypeScript
    /// project initialization, then `wait_ready()` when you need to use it.
    ///
    /// # Arguments
    /// * `sidecar_path` - Path to the sidecar executable (node script)
    ///
    /// # Returns
    /// A new `TypeSidecar` instance in the `Spawning` state.
    pub fn spawn(sidecar_path: &Path) -> Result<Self, SidecarError> {
        let spawn_time = Instant::now();

        debug!("[type_sidecar] Spawning sidecar from: {:?}", sidecar_path);

        // Spawn the Node.js process
        let mut child = Command::new("node")
            .arg(sidecar_path)
            .stdin(Stdio::piped())
            .stdout(Stdio::piped())
            .stderr(Stdio::piped()) // Capture stderr, pipe through tracing
            .spawn()
            .map_err(|e| SidecarError::SpawnFailed(e.to_string()))?;

        let stdin = child
            .stdin
            .take()
            .ok_or_else(|| SidecarError::SpawnFailed("Failed to get stdin".to_string()))?;

        let stdout = child
            .stdout
            .take()
            .ok_or_else(|| SidecarError::SpawnFailed("Failed to get stdout".to_string()))?;

        // Pipe sidecar stderr through tracing on a background thread
        if let Some(stderr) = child.stderr.take() {
            thread::spawn(move || {
                let reader = BufReader::new(stderr);
                for line in reader.lines().map_while(Result::ok) {
                    if !line.trim().is_empty() {
                        debug!("{}", line);
                    }
                }
            });
        }

        debug!(
            "[type_sidecar] Sidecar spawned in {:?}",
            spawn_time.elapsed()
        );

        Ok(Self {
            child,
            stdin: Mutex::new(stdin),
            stdout: Mutex::new(BufReader::new(stdout)),
            state: Arc::new(Mutex::new(SidecarState::Spawning)),
            spawn_time,
            request_counter: Mutex::new(0),
        })
    }

    /// Start initialization of the TypeScript project.
    ///
    /// This sends the init request and returns immediately. The sidecar
    /// will process the initialization in the background. Use `wait_ready()`
    /// to block until initialization is complete.
    ///
    /// # Arguments
    /// * `repo_root` - Path to the repository root
    /// * `tsconfig_path` - Optional path to tsconfig.json (relative to repo root)
    pub fn start_init(&self, repo_root: &Path, tsconfig_path: Option<&str>) {
        let repo_root_str = repo_root.to_string_lossy().to_string();
        let tsconfig = tsconfig_path.map(String::from);

        // Update state to Initializing
        {
            let mut state = self.state.lock().unwrap();
            *state = SidecarState::Initializing;
        }

        debug!("[type_sidecar] Starting init for repo: {}", repo_root_str);

        // Send init request
        let request = SidecarRequest::Init {
            request_id: self.next_request_id(),
            repo_root: repo_root_str,
            tsconfig_path: tsconfig,
        };

        if let Err(e) = self.send_request(&request) {
            let mut state = self.state.lock().unwrap();
            *state = SidecarState::Failed(format!("Failed to send init: {}", e));
        }
    }

    /// Check if the sidecar is ready (non-blocking).
    ///
    /// This checks the current state without waiting.
    pub fn is_ready(&self) -> bool {
        let state = self.state.lock().unwrap();
        *state == SidecarState::Ready
    }

    /// Get the current state of the sidecar.
    pub fn get_state(&self) -> SidecarState {
        self.state.lock().unwrap().clone()
    }

    /// Wait for the sidecar to become ready.
    ///
    /// This blocks until the sidecar is ready or the timeout expires.
    /// If `start_init()` was called, this will wait for the init response.
    ///
    /// # Arguments
    /// * `timeout` - Maximum time to wait
    ///
    /// # Returns
    /// Ok(()) if ready, Err if timeout or failure.
    pub fn wait_ready(&self, timeout: Duration) -> Result<(), SidecarError> {
        let start = Instant::now();

        // If we're in Initializing state, read the init response
        {
            let state = self.state.lock().unwrap();
            if *state == SidecarState::Initializing {
                drop(state); // Release lock before reading

                // Read the init response
                match self.read_response_with_timeout(timeout) {
                    Ok(response) => {
                        let mut state = self.state.lock().unwrap();
                        if response.status == "ready" {
                            *state = SidecarState::Ready;
                            debug!(
                                "[type_sidecar] Sidecar ready (init_time: {:?}ms, total: {:?})",
                                response.init_time_ms,
                                self.spawn_time.elapsed()
                            );
                        } else {
                            let error = response
                                .errors
                                .map(|e| e.join("; "))
                                .unwrap_or_else(|| "Unknown error".to_string());
                            *state = SidecarState::Failed(error.clone());
                            return Err(SidecarError::InitFailed(error));
                        }
                    }
                    Err(e) => {
                        let mut state = self.state.lock().unwrap();
                        *state = SidecarState::Failed(e.to_string());
                        return Err(e);
                    }
                }
            }
        }

        // Poll state until ready or timeout
        while start.elapsed() < timeout {
            let state = self.state.lock().unwrap();
            match &*state {
                SidecarState::Ready => return Ok(()),
                SidecarState::Failed(e) => return Err(SidecarError::InitFailed(e.clone())),
                _ => {
                    drop(state);
                    thread::sleep(Duration::from_millis(10));
                }
            }
        }

        Err(SidecarError::Timeout)
    }

    /// Resolve explicit types by bundling symbols.
    ///
    /// # Arguments
    /// * `symbols` - Symbols to bundle
    ///
    /// # Returns
    /// The sidecar response with bundled types.
    pub fn resolve_types(
        &self,
        symbols: &[SymbolRequest],
    ) -> Result<SidecarResponse, SidecarError> {
        self.ensure_ready()?;

        if symbols.is_empty() {
            return Ok(SidecarResponse {
                request_id: "empty".to_string(),
                status: "success".to_string(),
                init_time_ms: None,
                dts_content: Some(String::new()),
                manifest: Some(vec![]),
                symbol_failures: None,
                inferred_types: None,
                definitions: None,
                errors: None,
            });
        }

        let request = SidecarRequest::Bundle {
            request_id: self.next_request_id(),
            symbols: symbols.to_vec(),
        };

        self.send_request(&request)?;
        self.read_response_with_timeout(Duration::from_secs(60))
    }

    /// Infer implicit types at specified locations.
    ///
    /// # Arguments
    /// * `requests` - Inference requests
    /// * `extraction_config` - Optional agent-generated unwrap rules
    ///
    /// # Returns
    /// The sidecar response with inferred types.
    pub fn infer_types(
        &self,
        requests: &[InferRequestItem],
        extraction_config: Option<&ExtractionConfig>,
    ) -> Result<SidecarResponse, SidecarError> {
        self.ensure_ready()?;

        if requests.is_empty() {
            return Ok(SidecarResponse {
                request_id: "empty".to_string(),
                status: "success".to_string(),
                init_time_ms: None,
                dts_content: None,
                manifest: None,
                symbol_failures: None,
                inferred_types: Some(vec![]),
                definitions: None,
                errors: None,
            });
        }

        let request = SidecarRequest::Infer {
            request_id: self.next_request_id(),
            requests: requests.to_vec(),
            extraction_config: extraction_config
                .filter(|config| !config.rules.is_empty())
                .cloned(),
        };

        self.send_request(&request)?;
        self.read_response_with_timeout(Duration::from_secs(60))
    }

    /// Resolve type definitions from bundled .d.ts content.
    ///
    /// For each alias, returns both the original declaration text and the
    /// compiler-expanded form with all types fully inlined.
    ///
    /// # Arguments
    /// * `bundled_dts` - The bundled .d.ts content
    /// * `aliases` - Type alias names to resolve
    pub fn resolve_definitions(
        &self,
        bundled_dts: &str,
        aliases: &[String],
    ) -> Result<Vec<ResolvedDefinitionResult>, SidecarError> {
        self.ensure_ready()?;

        if aliases.is_empty() {
            return Ok(vec![]);
        }

        let request = SidecarRequest::ResolveDefinitions {
            request_id: self.next_request_id(),
            bundled_dts: bundled_dts.to_string(),
            aliases: aliases.to_vec(),
        };

        self.send_request(&request)?;
        let response = self.read_response_with_timeout(Duration::from_secs(30))?;

        if response.status != "success" {
            let errors = response.errors.unwrap_or_default();
            return Err(SidecarError::ResolutionFailed(errors.join("; ")));
        }

        Ok(response.definitions.unwrap_or_default())
    }

    /// Resolve all types (explicit + inferred) in a single operation.
    ///
    /// # Arguments
    /// * `explicit` - Symbols for explicit type bundling
    /// * `infer` - Requests for type inference
    /// * `extraction_config` - Optional agent-generated unwrap rules
    ///
    /// # Returns
    /// Combined result with both explicit and inferred types.
    pub fn resolve_all_types(
        &self,
        explicit: &[SymbolRequest],
        infer: &[InferRequestItem],
        extraction_config: Option<&ExtractionConfig>,
    ) -> Result<TypeResolutionResult, SidecarError> {
        self.ensure_ready()?;

        let mut result = TypeResolutionResult {
            dts_content: None,
            explicit_manifest: vec![],
            inferred_types: vec![],
            symbol_failures: vec![],
            errors: vec![],
        };

        // Infer implicit types FIRST: a successful inference is the compiler's
        // view of the actual use site, and it carries the resolved array depth
        // relative to its anchor symbol. The explicit bundle below needs that
        // depth before it runs (#306).
        if !infer.is_empty() {
            let infer_response = self.infer_types(infer, extraction_config)?;
            if let Some(inferred) = infer_response.inferred_types {
                result.inferred_types = inferred;
            }
            if let Some(errors) = infer_response.errors {
                result.errors.extend(errors);
            }
        }

        // Two-anchor arbitration (#413, pub/sub only): a request carrying the
        // scanner's deterministic borrow witness whose alias the inference
        // resolved to a DIFFERENT named root is demoted here — re-aimed at
        // the tsc-witnessed root so the bundler defines the alias from the
        // inferred payload type instead of the borrowed symbol. This must
        // happen BEFORE the bundle runs: compat verdicts are computed from
        // the bundled dts, so a post-hoc flag flip in
        // `enrich_manifest_with_type_resolution` could never undo a wrong
        // explicit definition.
        let explicit = demote_witnessed_borrowed_anchors(explicit, &result.inferred_types);

        // Bundle explicit types. An LLM-extracted anchor is a bare identifier
        // by schema contract (`User[]` → `User`), so bundling it as-is erases
        // the use-site's array-ness and an array-vs-scalar mismatch scores
        // compatible (#306). Copy the inference-resolved depth onto each
        // matching request so the bundler's array_depth wrap (#248) restores
        // the `[]` levels.
        let explicit = apply_inferred_array_depth(&explicit, &result.inferred_types);
        if !explicit.is_empty() {
            let bundle_response = self.resolve_types(&explicit)?;
            result.dts_content = bundle_response.dts_content;
            if let Some(manifest) = bundle_response.manifest {
                result.explicit_manifest = manifest;
            }
            if let Some(failures) = bundle_response.symbol_failures {
                result.symbol_failures = failures;
            }
            if let Some(errors) = bundle_response.errors {
                result.errors.extend(errors);
            }
        }

        let had_explicit_dts = result.dts_content.is_some();
        let mut combined_dts = result.dts_content.take().unwrap_or_default();
        let mut appended_aliases: HashSet<String> = HashSet::new();

        // Pre-populate with aliases already defined by the bundler so the
        // infer / failure-fallback loops below never shadow them with `= unknown`.
        if had_explicit_dts {
            for req in &explicit {
                if let Some(alias) = &req.alias
                    && Self::dts_defines_alias(&combined_dts, alias)
                {
                    appended_aliases.insert(alias.clone());
                }
                if Self::dts_defines_alias(&combined_dts, &req.symbol_name) {
                    appended_aliases.insert(req.symbol_name.clone());
                }
            }
        }

        let mut append_alias = |alias: &str, type_string: &str| -> bool {
            if !appended_aliases.insert(alias.to_string()) {
                return false;
            }
            if !combined_dts.is_empty() && !combined_dts.ends_with('\n') {
                combined_dts.push('\n');
            }
            combined_dts.push_str("export type ");
            combined_dts.push_str(alias);
            combined_dts.push_str(" = ");
            combined_dts.push_str(type_string.trim().trim_end_matches(';'));
            combined_dts.push_str(";\n");
            true
        };

        let mut inferred_aliases = HashSet::new();
        for inferred in &result.inferred_types {
            inferred_aliases.insert(inferred.alias.clone());
            let type_string = if Self::is_untyped_response_type(&inferred.type_string) {
                "unknown"
            } else {
                inferred.type_string.as_str()
            };
            append_alias(&inferred.alias, type_string);
        }

        // Scrub inferred_types so downstream consumers (enrich_manifest_with_type_resolution)
        // see "unknown" for framework types and leave type_state as Unknown.
        for inferred in &mut result.inferred_types {
            if Self::is_untyped_response_type(&inferred.type_string) {
                inferred.type_string = "unknown".to_string();
            }
        }

        for failure in &result.symbol_failures {
            if let Some(request) = explicit.iter().find(|req| {
                req.symbol_name == failure.symbol_name && req.source_file == failure.source_file
            }) {
                let alias = request
                    .alias
                    .clone()
                    .unwrap_or_else(|| request.symbol_name.clone());
                append_alias(&alias, "unknown");
            }
        }

        if !infer.is_empty() {
            for request in infer {
                if let Some(alias) = &request.alias
                    && !inferred_aliases.contains(alias)
                {
                    append_alias(alias, "unknown");
                }
            }
        }

        if had_explicit_dts || !combined_dts.is_empty() {
            result.dts_content = Some(combined_dts);
        }

        Ok(result)
    }

    /// Check health status of the sidecar.
    pub fn health_check(&self) -> Result<SidecarResponse, SidecarError> {
        let request = SidecarRequest::Health {
            request_id: self.next_request_id(),
        };

        self.send_request(&request)?;
        self.read_response_with_timeout(Duration::from_secs(5))
    }

    /// Shutdown the sidecar gracefully.
    pub fn shutdown(&self) -> Result<(), SidecarError> {
        let request = SidecarRequest::Shutdown {
            request_id: self.next_request_id(),
        };

        // Send shutdown, ignore response (process will exit)
        let _ = self.send_request(&request);
        Ok(())
    }

    // ========================================================================
    // Internal Methods
    // ========================================================================

    fn next_request_id(&self) -> String {
        let mut counter = self.request_counter.lock().unwrap();
        *counter += 1;
        format!("req-{}", counter)
    }

    /// Check whether a bundled DTS string already defines a given alias
    /// (as a type, interface, class, enum, or namespace declaration).
    fn dts_defines_alias(content: &str, alias: &str) -> bool {
        let escaped = regex::escape(alias);
        let pattern = format!(r"\b(type|interface|class|enum|namespace)\s+{}\b", escaped);
        match regex::Regex::new(&pattern) {
            Ok(re) => re.is_match(content),
            Err(_) => false,
        }
    }

    fn is_untyped_response_type(type_string: &str) -> bool {
        let trimmed = type_string.trim().trim_end_matches(';');
        if trimmed.is_empty() {
            return false;
        }
        let base = trimmed.rsplit('.').next().unwrap_or(trimmed);
        matches!(
            base,
            // Express
            "Response" | "Request" | "NextFunction" |
            "Express" | "Application" | "Router" |
            // Fastify
            "Context" | "FastifyInstance" | "FastifyReply" | "FastifyRequest" |
            // Koa
            "Koa" | "ParameterizedContext" |
            // Hapi
            "ResponseToolkit" | "ResponseObject" | "Lifecycle" |
            // NestJS
            "ExecutionContext" | "ArgumentsHost" | "CallHandler" | "HttpException" |
            // Hono
            "Hono" | "HonoRequest" |
            // Node.js HTTP core
            "IncomingMessage" | "ServerResponse"
        )
    }

    fn ensure_ready(&self) -> Result<(), SidecarError> {
        let state = self.state.lock().unwrap();
        match &*state {
            SidecarState::Ready => Ok(()),
            SidecarState::Failed(e) => Err(SidecarError::NotReady(e.clone())),
            _ => Err(SidecarError::NotReady(
                "Sidecar not initialized. Call start_init() and wait_ready() first.".to_string(),
            )),
        }
    }

    fn send_request(&self, request: &SidecarRequest) -> Result<(), SidecarError> {
        let json = serde_json::to_string(request)
            .map_err(|e| SidecarError::SerializationError(e.to_string()))?;

        let mut stdin = self.stdin.lock().unwrap();
        writeln!(stdin, "{}", json).map_err(|e| SidecarError::IoError(e.to_string()))?;
        stdin
            .flush()
            .map_err(|e| SidecarError::IoError(e.to_string()))?;

        Ok(())
    }

    fn read_response_with_timeout(
        &self,
        timeout: Duration,
    ) -> Result<SidecarResponse, SidecarError> {
        let mut stdout = self.stdout.lock().unwrap();
        let mut line = String::new();

        // Set up timeout using a simple polling approach
        // Note: For production, consider using async I/O
        let start = Instant::now();

        loop {
            if start.elapsed() > timeout {
                return Err(SidecarError::Timeout);
            }

            // Try to read a line
            match stdout.read_line(&mut line) {
                Ok(0) => {
                    // EOF - process may have died
                    return Err(SidecarError::ProcessDied);
                }
                Ok(_) => {
                    // Got a line, parse it
                    let trimmed = line.trim();
                    if trimmed.is_empty() {
                        line.clear();
                        continue;
                    }

                    let response: SidecarResponse = serde_json::from_str(trimmed).map_err(|e| {
                        SidecarError::DeserializationError(format!("{}: {}", e, trimmed))
                    })?;

                    return Ok(response);
                }
                Err(ref e) if e.kind() == std::io::ErrorKind::WouldBlock => {
                    // No data yet, sleep a bit
                    thread::sleep(Duration::from_millis(10));
                    continue;
                }
                Err(e) => {
                    return Err(SidecarError::IoError(e.to_string()));
                }
            }
        }
    }
}

/// Two-anchor arbitration for pub/sub payload anchors (#413, the #403/#359
/// borrow-class lineage): demote a witnessed-borrowed explicit request when
/// the same alias's location-based inference resolved a DIFFERENT named root.
///
/// A pub/sub op can carry two independent anchors: the LLM's explicit
/// `primary_type_symbol` (a judgment, measured wrong 13/20 on the honest
/// borrow fixture) and the payload locator, which the sidecar resolves
/// deterministically with tsc. When both resolve, the per-alias inferred root
/// symbol is compared against the explicit request's symbol — the same
/// alias+symbol join `apply_inferred_array_depth` performs. On a named-root
/// disagreement the explicit request is demoted: re-aimed at the inferred
/// root (symbol, declaration file, and array depth all from the inference),
/// so the bundler defines the alias from the tsc-witnessed payload type
/// instead of the borrowed one. Re-aiming rather than dropping keeps the
/// alias's dts definition self-contained — the inferred type_string keeps
/// named types as names (`OrderPlaced`), which would dangle if pasted in as
/// a standalone `export type` — while still making the inferred type the
/// alias's definition.
///
/// Demotion is gated on the scanner's deterministic borrow witness
/// (`payload_borrow_witness`, computed from the file's AST in
/// `collect_pubsub_type_requests`): no witness, no demotion — a bare
/// tsc-vs-LLM disagreement without AST evidence keeps the explicit symbol,
/// so a mis-anchored inference (wrong text occurrence, envelope resolution)
/// can never displace a correct explicit anchor on its own.
///
/// Anonymous inferred types are NOT arbitrated: when the inference returns a
/// structural type with no root symbol (`primary_type_symbol: None` — object
/// literals, encoded payloads, lib-global roots), the named-root-disagreement
/// rule cannot fire and the explicit symbol is kept, witness or not. There is
/// deliberately no resolution for that case — a structural shape carries no
/// identity to disagree with, and inventing a comparison (e.g. shape
/// equality) would demote on formatting noise.
///
/// Further keeps, all deliberate:
/// - an alias carried by more than one explicit request (fan-in ambiguity —
///   the inference cannot be attributed to either request);
/// - an inference whose type_string is not the bare named form of its own
///   root (`Root`, `Root[]`, …): a generic instantiation
///   (`Envelope<Order>`) or an expanded structural shape cannot be
///   reproduced by bundling the bare root symbol, so re-aiming would change
///   the type;
/// - a root with no reported declaration file (the bundler could not resolve
///   it), or a framework envelope root (demotion must never install
///   machinery as a payload);
/// - requests without the witness flag — every HTTP/socket/GraphQL request,
///   making this function structurally inert outside pub/sub.
fn demote_witnessed_borrowed_anchors(
    explicit: &[SymbolRequest],
    inferred: &[InferredType],
) -> Vec<SymbolRequest> {
    // First inference per alias wins, mirroring `apply_inferred_array_depth`
    // and the `or_insert` join in `enrich_manifest_with_type_resolution`.
    let mut inferred_by_alias: HashMap<&str, &InferredType> = HashMap::new();
    for inf in inferred {
        inferred_by_alias.entry(inf.alias.as_str()).or_insert(inf);
    }

    let mut alias_counts: HashMap<&str, usize> = HashMap::new();
    for req in explicit {
        if let Some(alias) = req.alias.as_deref() {
            *alias_counts.entry(alias).or_insert(0) += 1;
        }
    }

    explicit
        .iter()
        .map(|req| {
            if !req.payload_borrow_witness {
                return req.clone();
            }
            let Some(alias) = req.alias.as_deref() else {
                return req.clone();
            };
            if alias_counts.get(alias).copied().unwrap_or(0) > 1 {
                return req.clone();
            }
            let Some(inf) = inferred_by_alias.get(alias) else {
                return req.clone();
            };
            // Anonymous inferred type: no root to disagree with — keep.
            let Some(root) = inf.primary_type_symbol.as_deref() else {
                return req.clone();
            };
            if root == req.symbol_name {
                return req.clone();
            }
            // Re-aiming needs the root's declaration file.
            let Some(root_source) = inf.primary_type_symbol_source.as_deref() else {
                return req.clone();
            };
            // Never install machinery as a payload.
            if TypeSidecar::is_untyped_response_type(root) {
                return req.clone();
            }
            // The inference must BE the bare named form of its root (modulo
            // array suffixes), or bundling the root would change the type.
            let mut named_form = inf.type_string.trim();
            while let Some(stripped) = named_form.strip_suffix("[]") {
                named_form = stripped.trim_end();
            }
            if named_form != root {
                return req.clone();
            }
            info!(
                alias = %alias,
                explicit_symbol = %req.symbol_name,
                inferred_root = %root,
                inferred_root_source = %root_source,
                "[type_sidecar] pub/sub anchor arbitration: witnessed borrowed \
                 symbol disagrees with the tsc-resolved payload root; demoting \
                 the explicit anchor to the inferred type"
            );
            SymbolRequest {
                symbol_name: root.to_string(),
                source_file: root_source.to_string(),
                alias: req.alias.clone(),
                array_depth: inf.array_depth,
                payload_borrow_witness: false,
            }
        })
        .collect()
}

/// Copy inference-resolved array depths onto explicit symbol requests (#306).
///
/// An LLM-extracted anchor is a bare identifier by schema contract (`User[]` →
/// `User`), so an explicit bundle built from it erases the use-site's
/// array-ness: the producer comparand for a handler returning
/// `TimelineEvent[]` becomes the bare element and an array-vs-scalar mismatch
/// passes assignability. The inference that runs for the SAME alias resolves
/// the actual use site through ts-morph and reports the element symbol plus
/// the peeled depth, so when alias and symbol agree the depth is deterministic
/// ground truth for the request. Depths already set by the caller (the GraphQL
/// SDL list marker, #248) are never overwritten; a symbol disagreement means
/// the anchor doesn't describe the inferred type, so the request is left
/// untouched.
fn apply_inferred_array_depth(
    explicit: &[SymbolRequest],
    inferred: &[InferredType],
) -> Vec<SymbolRequest> {
    // First anchor-carrying inference per alias wins, mirroring the
    // `or_insert` join `enrich_manifest_with_type_resolution` uses.
    let mut depth_by_alias: HashMap<&str, (&str, u32)> = HashMap::new();
    for inf in inferred {
        if let (Some(symbol), Some(depth)) = (inf.primary_type_symbol.as_deref(), inf.array_depth)
            && depth > 0
        {
            depth_by_alias
                .entry(inf.alias.as_str())
                .or_insert((symbol, depth));
        }
    }

    explicit
        .iter()
        .cloned()
        .map(|mut req| {
            if req.array_depth.is_none()
                && let Some(alias) = req.alias.as_deref()
                && let Some((symbol, depth)) = depth_by_alias.get(alias)
                && *symbol == req.symbol_name
            {
                req.array_depth = Some(*depth);
            }
            req
        })
        .collect()
}

impl Drop for TypeSidecar {
    fn drop(&mut self) {
        debug!("[type_sidecar] Shutting down sidecar");

        // Try graceful shutdown first
        let _ = self.shutdown();

        // Give it a moment to exit
        thread::sleep(Duration::from_millis(100));

        // Force kill if still running
        let _ = self.child.kill();
    }
}

